package updateResult;

public class updateResult {
	
	private String rid;
	private String sid;
	private String qType;
	private String marks;
	
	
	public updateResult(String rid, String sid, String qType, String marks) {
		this.rid = rid;
		this.sid = sid;
		this.qType = qType;
		this.marks = marks;
	}


	public String getRid() {
		return rid;
	}


	public String getSid() {
		return sid;
	}


	public String getqType() {
		return qType;
	}


	public String getMarks() {
		return marks;
	}
	
	
	
	

}
